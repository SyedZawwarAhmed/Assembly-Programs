DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
FIRSTDIGIT DB 0
SECONDDIGIT DB 0
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
ADD FIRSTDIGIT,48
ADD SECONDDIGIT,48
MOV CX,10
L1:
MOV SECONDDIGIT, "0"
PUSH CX
MOV CX,10
L2:
MOV DL,FIRSTDIGIT
MOV AH,2
INT 21H
MOV DL,SECONDDIGIT
MOV AH,2
INT 21H
MOV DX,10
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H
INC SECONDDIGIT
LOOP L2
POP CX
INC FIRSTDIGIT
LOOP L1
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
