DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
STR1 DB "APPLE$"
STR2 DB "BANANA$"
STR3 DB "ORANGE$"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET STR1
MOV AH,9
INT 21H
CALL ENTERKEY
MOV DX,OFFSET STR2
MOV AH,9
INT 21H
CALL ENTERKEY
MOV DX,OFFSET STR3
MOV AH,9
INT 21H
CALL ENTERKEY
MOV AH,4CH
INT 21H
MAIN ENDP
ENTERKEY PROC
MOV DX,10
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H
RET
ENTERKEY ENDP
END MAIN
